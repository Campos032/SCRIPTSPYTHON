# Variável composta, listas diferentemente das tuplas, são mutáveis.
# Ao utilizar um método em sua lista ela se reorganiza automaticamente, reordenando os índices
# Alguns comandos básicos de listas são:
# lista.append(item) para adicionar algo a lista na última posição
# lista.insert(índice, item) para adicionar algo na lista no índice que você desejar
# del lista[índice] para remover algo da lista conforme o índice
# lista.pop() remove o último item da lista ou, lista.pop(índice) para remover o índice indicado
# lista.remove(item) remove aquele índices que possui item, para excluir todos, faça um laço de repetição
# valor = list(range(4,11)) cria uma lista com os valores do range
# valor.sort() ordena os valores de forma crescente
# valor.sort(reverse=True) ordena de forma decrescente
# len(valor) conta o tamanho da lista
# b = a[:] para fazer a cópia de uma lista, e não b = a (Dessa maneira as duas listas seriam interligadas)
