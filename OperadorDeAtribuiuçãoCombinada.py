# Operadores de atribuição combinada nada mais é que

# Em Python, o operador += é um operador de atribuição combinada que adiciona o valor à variável e, em seguida, atribui
# o resultado à variável. Em outras palavras, ele é uma forma concisa de escrever x = x + y, onde x e y são variáveis.

# Neste exemplo, x += y adiciona o valor de y a x e atribui o resultado de volta a x. O mesmo conceito se aplica a
# outros operadores aritméticos, como -, *, /, etc.

# Essa notação permite tornar o código mais conciso e legível, especialmente quando você está atualizando variáveis
# com base em seus próprios valores anteriores.

x = 5
y = 3
x += y  # Isso é equivalente a x = x + y
print(x)  # O resultado será 8

a = 5
c = 4

a *= c  # a = a * c
print(a)  # Resultado = 20
